public class EnhancFor {
    public static void main(String[] args) {
        // This is an explanation for enhanced for loop
        // This kind of loop is mainly used for Arrays and other iterable stuff 
        int [] arr = {1,2,3,4,5,6}; 
        for(int num : arr)
        {
            System.out.println(num);
        }
    }
}
